/// Defines the side on which the list wheel's curve will appear.
enum CurvedListWheelSide {
  /// The curve is on the left, and items scroll vertically.
  left,

  /// The curve is on the right, and items scroll vertically.
  right,

  /// The curve is on the top, and items scroll horizontally.
  top,

  /// The curve is on the bottom, and items scroll horizontally.
  bottom,
}
